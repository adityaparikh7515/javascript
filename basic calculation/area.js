// Area of Square
let side = Number(prompt("enter side "))
let areaSquare = side * side;
console.log("Area of Square:", areaSquare);

// Area of Triangle
let base =Number(prompt("enter size of base"))
let height = Number(prompt("enter height"));
let areaTriangle = 0.5 * base * height;
console.log("Area of Triangle:", areaTriangle);

// Area of Rectangle
let length = Number(prompt("enter length"));
let breadth = Number(prompt("enter breadth"));
let areaRectangle = length * breadth;
console.log("Area of Rectangle:", areaRectangle);

// Area of Circle
let radius =Number(prompt("enter radius size"))
let areaCircle = 3.14 * radius * radius;
console.log("Area of Circle:", areaCircle);