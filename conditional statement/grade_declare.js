// Grade	Description
// E	Excellent
// V	Very Good
// G	Good
// A	Average
// F	Fail

let ch = prompt("enter your grade")
if (ch == 'A') {
    alert("Average")
} else if (ch == 'G') {
    alert("Good")
} else if (ch == 'V') {
    alert("Very good")
} else if (ch == 'F') {
    alert("Fail")
} else if (ch == 'E') {
    alert("Excellent")
} else {
    alert("invalid")
}
