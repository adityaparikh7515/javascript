// ->Day Number to Day Name

let daynumber = Number(prompt("enter your day number"))
if (daynumber == 1) {
    alert("monday")
} else if (daynumber == 2) {
    alert("tuesday")
} else if (daynumber == 3) {
    alert("wednesday")
} else if (daynumber == 4) {
    alert("thursday")
} else if (daynumber == 5) {
    alert("friday")
} else if (daynumber == 6) {
    alert("saturday")
} else if (daynumber == 7) {
    alert("sunday")
} else {
    alert("invalid Number ")
}
